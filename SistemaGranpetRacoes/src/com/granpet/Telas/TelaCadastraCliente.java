    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String opcao = btnSalvar.getText();
        if (opcao == "Novo") {
            btnSalvar.setText("Salvar");
            txtDono.setEnabled(true);
            txtFone.setEnabled(true);
            txtDaNa.setEnabled(true);
            txtEndereco.setEnabled(true);
            txtObs.setEnabled(true);
            txtRaca.setEnabled(true);
        } else {
            if (txtNome.getText().isEmpty() || txtDono.getText().isEmpty()
                    || txtFone.getText().isEmpty() || txtDaNa.getText().isEmpty()
                    || txtRaca.getText().isEmpty() || txtEndereco.getText().isEmpty() || txtObs.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchido para salvar um novo cliente", "Aviso", 0);
            } else {
                cliente.setNomeCliente(txtNome.getText());
                cliente.setRacaCliente(txtRaca.getText());
                cliente.setProprCliente(txtDono.getText());
                cliente.setFoneCliente(txtFone.getText());
                cliente.setEnderecoCliente(txtEndereco.getText());
                cliente.setDataNascimento(fd.FormataData(txtDaNa.getText()));
                cliente.setObsCliente(txtObs.getText());
                fc.SalvaCliente(cliente);
                int cadastraNovo = JOptionPane.showConfirmDialog(null, "Cliente Cadastrado com sucesso, Deseja Cadastrar um novo Cliente?", "Aviso", JOptionPane.YES_NO_OPTION);
                if (cadastraNovo == JOptionPane.YES_OPTION) {
                    txtNome.setText("");
                    txtDono.setText("");
                    txtFone.setText("");
                    txtDaNa.setText("");
                    txtEndereco.setText("");
                    txtRaca.setText("");
                    txtObs.setText("");
                } else {
                    btnExcluir.setEnabled(false);
                    btnSalvar.setText("Novo");
                    txtDono.setEnabled(false);
                    txtFone.setEnabled(false);
                    txtDaNa.setEnabled(false);
                    txtEndereco.setEnabled(false);
                    txtObs.setEnabled(false);
                    txtRaca.setEnabled(false);
                    txtNome.setText("");
                    txtDono.setText("");
                    txtFone.setText("");
                    txtDaNa.setText("");
                    txtEndereco.setText("");
                    txtRaca.setText("");
                    txtObs.setText("");
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        cliente = fc.PesquisaCliente(txtNome.getText());
        if (btnPesquisar.getText() == "Pesquisar") {
            if (cliente == null) {
                int cadastra = JOptionPane.showConfirmDialog(null, "Cliente não encontrado, Deseja Cadastrar?", "Aviso", JOptionPane.YES_NO_OPTION);
                if (cadastra == JOptionPane.YES_OPTION) {
                    btnSalvar.setText("Salvar");
                    txtDono.setEnabled(true);
                    txtFone.setEnabled(true);
                    txtDaNa.setEnabled(true);
                    txtEndereco.setEnabled(true);
                    txtObs.setEnabled(true);
                    txtRaca.setEnabled(true);
                }
            } else {
                txtNome.setText(cliente.getNomeCliente());
                txtDaNa.setText(fd.DataBr(cliente.getDataNascimento()));
                txtEndereco.setText(cliente.getEnderecoCliente());
                txtObs.setText(cliente.getObsCliente());
                txtDono.setText(cliente.getProprCliente());
                txtFone.setText(cliente.getFoneCliente());
                txtRaca.setText(cliente.getRacaCliente());
                btnPesquisar.setText("Atualizar");
                txtDono.setEnabled(true);
                txtFone.setEnabled(true);
                txtDaNa.setEnabled(true);
                txtEndereco.setEnabled(true);
                txtObs.setEnabled(true);
                txtRaca.setEnabled(true);
                btnExcluir.setEnabled(true);
            }
        } else {
            int atualiza = JOptionPane.showConfirmDialog(null, "Deseja atualizar os dados do Cliente", "Aviso", JOptionPane.YES_NO_OPTION);
            if (atualiza == JOptionPane.YES_OPTION) {
                cliente.setNomeCliente(txtNome.getText());
                cliente.setProprCliente(txtDono.getText());
                cliente.setRacaCliente(txtRaca.getText());
                cliente.setFoneCliente(txtFone.getText());
                fc.AtualizaCliente(cliente);
                btnExcluir.setEnabled(false);
                btnPesquisar.setText("Pesquisar");
                txtDono.setEnabled(false);
                txtFone.setEnabled(false);
                txtDaNa.setEnabled(false);
                txtEndereco.setEnabled(false);
                txtObs.setEnabled(false);
                txtRaca.setEnabled(false);
                txtNome.setText("");
                txtDono.setText("");
                txtFone.setText("");
                txtDaNa.setText("");
                txtEndereco.setText("");
                txtRaca.setText("");
                txtObs.setText("");
            } else {
                txtDono.setEnabled(true);
                txtFone.setEnabled(true);
                txtDaNa.setEnabled(true);
                txtEndereco.setEnabled(true);
                txtObs.setEnabled(true);
                txtRaca.setEnabled(true);
                btnExcluir.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

