    private void PreencherTabela() {
        ArrayList clientes = new ArrayList();
        String sql = "SELECT * FROM `tblcliente`";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                cliente.setNomeCliente(rs.getString("nomeCliente"));
                cliente.setProprCliente(rs.getString("proprietarioCliente"));
                cliente.setFoneCliente(rs.getString("foneCliente"));
                cliente.setRacaCliente(rs.getString("racaCliente"));
                System.out.println(cliente);
                clientes.add(cliente);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro no Banco de dados " + e, "Atenção", 0);
        }
        String[] colunas = new String[]{"Nome", "Proprietario", "Telefone", "Raça"};
        System.out.println(clientes);
        tblModelo.setNcolunas(colunas.length);
        //tblClientes.setModel(mtbl);
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(160);
        tblClientes.getColumnModel().getColumn(0).setResizable(false);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(278);
        tblClientes.getColumnModel().getColumn(1).setResizable(false);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblClientes.getColumnModel().getColumn(2).setResizable(false);
        tblClientes.getColumnModel().getColumn(3).setPreferredWidth(100);
        tblClientes.getColumnModel().getColumn(3).setResizable(false);
        tblClientes.getTableHeader().setReorderingAllowed(false);
        tblClientes.setAutoResizeMode(tblClientes.AUTO_RESIZE_OFF);
        //tblClientes.setSelectionModel((int)ListSelectionModel.SINGLE_SELECTION);
    }

