    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String funcao = btnSalvar.getText();
        if (funcao == "Novo") {
            btnSalvar.setText("Salvar");
            txtMarca.setEnabled(true);
            txtCategoria.setEnabled(true);
            txtValidade.setEnabled(true);
            txtQTD.setEnabled(true);
            txtValorCompra.setEnabled(true);
            txtLucro.setEnabled(true);
            txtValorVenda.setEnabled(true);
        } else {
            if (txtCategoria.getText().isEmpty() || txtLucro.getText().isEmpty() || txtMarca.getText().isEmpty()
                    || txtProduto.getText().isEmpty() || txtQTD.getText().isEmpty() || txtValidade.getText().isEmpty()
                    || txtValorCompra.getText().isEmpty() || txtValorVenda.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de salvar", "Aviso", 0);
            } else {
                produto.setNomeProduto(txtProduto.getText());
                produto.setMarcaProduto(txtMarca.getText());
                produto.setDataValidade(fd.FormataData(txtValidade.getText()));
                produto.setTipoProduto(txtCategoria.getText());
                produto.setQtdProduto(Integer.parseInt(txtQTD.getText()));
                produto.setValorCompra(Double.parseDouble(fn.ConverterFloat(txtValorCompra.getText())));
                produto.setLucroPercent(Integer.parseInt(txtLucro.getText()));
                produto.setValorVenda(Double.parseDouble(fn.ConverterFloat(txtValorVenda.getText())));
                boolean salvou = fp.CadastraProduto(produto);
                if (salvou) {
                    int mais1 = JOptionPane.showConfirmDialog(null, "Produto Salvo com sucesso", "Aviso", JOptionPane.YES_NO_OPTION);
                    if (mais1 == JOptionPane.YES_OPTION) {
                        txtCategoria.setText("");
                        txtLucro.setText("");
                        txtMarca.setText("");
                        txtProduto.setText("");
                        txtQTD.setText("");
                        txtValidade.setText("");
                        txtValorCompra.setText("");
                        txtValorVenda.setText("");
                    } else {
                        btnSalvar.setText("Novo");
                        txtMarca.setEnabled(false);
                        txtCategoria.setEnabled(false);
                        txtValidade.setEnabled(false);
                        txtQTD.setEnabled(false);
                        txtValorCompra.setEnabled(false);
                        txtLucro.setEnabled(false);
                        txtValorVenda.setEnabled(false);
                        txtCategoria.setText("");
                        txtLucro.setText("");
                        txtMarca.setText("");
                        txtProduto.setText("");
                        txtQTD.setText("");
                        txtValidade.setText("");
                        txtValorCompra.setText("");
                        txtValorVenda.setText("");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar o produto", "Aviso", 0);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtLucroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtLucroFocusLost
        // TODO add your handling code here:
        String vcompra = fn.ConverterFloat(txtValorCompra.getText());
        float compra = Float.parseFloat(vcompra);
        float percento = Float.parseFloat(txtLucro.getText());
        float venda = compra + ((compra * percento) / 100);
        String valorVenda = df.format(venda);
        String valorImpresso = fn.FloatConvertido(valorVenda);
        txtValorVenda.setText(valorImpresso);
    }//GEN-LAST:event_txtLucroFocusLost

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        String funcao = btnPesquisar.getText();
        if (funcao == "Pesquisar") {
            produto = fp.BuscaProduto(txtProduto.getText());
            if (produto == null) {
                int naoEncontrado = JOptionPane.showConfirmDialog(null, "Produto n√£o encontrado, Deseja cadastrar um novo Produto?", "Aviso", JOptionPane.YES_NO_OPTION);
                if (naoEncontrado == JOptionPane.YES_OPTION) {
                    btnSalvar.setText("Salvar");
                    txtMarca.setEnabled(true);
                    txtCategoria.setEnabled(true);
                    txtValidade.setEnabled(true);
                    txtQTD.setEnabled(true);
                    txtValorCompra.setEnabled(true);
                    txtLucro.setEnabled(true);
                    txtValorVenda.setEnabled(true);
                }
            } else {
                txtCategoria.setText(produto.getTipoProduto());
                txtLucro.setText(Integer.toString(produto.getLucroPercent()));
                txtQTD.setText(Integer.toString(produto.getQtdProduto()));
                txtValorCompra.setText(Double.toString(produto.getValorCompra()));
                txtValorVenda.setText(Double.toString(produto.getValorVenda()));
                txtValidade.setText(fd.DataBr(produto.getDataValidade()));
                txtMarca.setText(produto.getMarcaProduto());
                btnPesquisar.setText("Atualizar");
                btnExcluir.setEnabled(true);
            }
        } else {
            produto.setNomeProduto(txtProduto.getText());
            produto.setMarcaProduto(txtMarca.getText());
            produto.setDataValidade(fd.FormataData(txtValidade.getText()));
            produto.setTipoProduto(txtCategoria.getText());
            produto.setValorCompra(Double.parseDouble(fn.ConverterFloat(txtValorCompra.getText())));
            produto.setQtdProduto(Integer.parseInt(txtQTD.getText()));
            produto.setValorVenda(Double.parseDouble(fn.ConverterFloat(txtValorVenda.getText())));
            fp.AtualizaProduto(produto);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int exclui = JOptionPane.showConfirmDialog(null, "Deseja Excluir esse produto:", "Alerta", JOptionPane.YES_NO_OPTION);
        if (exclui == JOptionPane.YES_OPTION) {
            produto = fp.BuscaProduto(txtProduto.getText());
            fp.ExcluiProduto(produto.getIdProduto());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


